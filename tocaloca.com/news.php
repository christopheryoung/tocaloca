<h1>Furious contemporary music trio Toca Loca is ready to unveil "The P*P Project"</h1>

<p>The P*P project examines the fierce and fearsome taxonomy that historically has made some composers soulfully classical, and others popular.  The cutting-edge of Canada's composers write P*P-length, P*P-inspired works, the All-Stars of jazz throw their hats in the ring and some of Canada's most interesting indie-rockers flex their contemporary music chops.</p>

 
